public class Student {
    private String name;
    private String surname;
    private int age;

    public Student setName(String name) {
        this.name = name;
        return this;
    }

    public Student setSurname(String surname) {
        this.surname = surname;
        return this;
    }

    public Student setAge(int age) {
        this.age = age;
        return this;
    }
}

